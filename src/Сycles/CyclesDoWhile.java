package Сycles;

public class CyclesDoWhile {
    public static void main(String[] args) {

        //Переменная а со значением 0
        int a = 0;

        //Цикл do while - выполняет код за фигурными скобками, а потом после
        do {
            System.out.println("Привет!");
        }

        //Цикл - пока а больше 0
        while (a > 0);
    }
}
